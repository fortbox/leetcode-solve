    public List<List<Integer>> pathWithObstacles(int[][] obstacleGrid) {
        Queue<Coordinate> queue = new LinkedList<>();
        if (obstacleGrid[0][0] == 0) queue.add(new Coordinate(0, 0));
        boolean[][] isVisited = new boolean[obstacleGrid.length][obstacleGrid[0].length];
        isVisited[0][0] = true;
        while (!queue.isEmpty()) {
            int size = queue.size();
            System.out.println("size = " + size);
            for (int i = 0; i < size; i++) {
                Coordinate poll = queue.poll();
                int x = poll.x;
                int y = poll.y;
                if (x == obstacleGrid.length - 1 && y == obstacleGrid[0].length - 1) return poll.steps;
                if (x + 1 < obstacleGrid.length && !isVisited[x + 1][y] && obstacleGrid[x + 1][y] == 0) {
                    queue.add(new Coordinate(x + 1, y, poll.steps));
                    isVisited[x + 1][y] = true;
                }
                if (y + 1 < obstacleGrid[0].length && !isVisited[x][y + 1] && obstacleGrid[x][y + 1] == 0) {
                    queue.add(new Coordinate(x, y + 1, poll.steps));
                    isVisited[x][y + 1] = true;
                }
            }
        }
        return new ArrayList<>();
    }

    class Coordinate {
        int x;
        int y;
        List<List<Integer>> steps = new ArrayList<>();


        public Coordinate(int x, int y) {
            this.x = x;
            this.y = y;
            this.steps.add(Arrays.asList(x, y));
        }

        public Coordinate(int x, int y, List<List<Integer>> list) {
            this.x = x;
            this.y = y;
            this.steps.addAll(list);
            this.steps.add(Arrays.asList(x, y));

        }

        public Coordinate() {
            steps = new ArrayList<>();
        }
    }
